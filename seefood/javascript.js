function ss1() {
    var num = prompt("How many sandwiches do you need?");
    if (num>=1) {
        document.getElementById("test1").innerHTML = "You ordered " + num + " sandwiches";  
    }else if (!num || num <= 0) {
        alert("Please Enter A valid number");
    }
}
